package de.juyas.margas.api.creature;

/**
 * Enum PotionEffectApplicationCause to represent the cause of a potion effect application.
 */
public enum PotionEffectApplicationCause {

    /**
     * The potion effect is applied when the creature is spawned.
     */
    SPAWN,

    /**
     * The potion effect is applied when the creature is attacked.
     */
    ATTACK

}
